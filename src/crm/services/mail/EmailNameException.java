package crm.services.mail;

public class EmailNameException extends Exception{

	public EmailNameException() {
		super();
	}

	public EmailNameException(String message, Throwable cause) {
		super(message, cause);
	}

	public EmailNameException(String message) {
		super(message);
	}

	public EmailNameException(Throwable cause) {
		super(cause);
	}

}
